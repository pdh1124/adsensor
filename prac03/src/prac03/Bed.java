package prac03;

public class Bed {
	
	int pillow;
	String blanket;
	String mattress;
	boolean ElectricPad;
	boolean Towel;
	
	
	public int getPillow() {
		return pillow;
	}
	public void setPillow(int pillow) {
		this.pillow = pillow;
	}
	public String getBlanket() {
		return blanket;
	}
	public void setBlanket(String blanket) {
		this.blanket = blanket;
	}
	public String getMattress() {
		return mattress;
	}
	public void setMattress(String mattress) {
		this.mattress = mattress;
	}
	public boolean getElectricPad() {
		return ElectricPad;
	}
	public void setElectricPad(boolean electricPad) {
		ElectricPad = electricPad;
	}
	public boolean getTowel() {
		return Towel;
	}
	public void setTowel(boolean towel) {
		Towel = towel;
	}
	
	
}
